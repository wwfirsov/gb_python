a = int(input('Результат в первый день:')) # строка
b = int(input('Километры:')) # строка
e = a
d = 1

print(d,"-й день результат: ", e)
while e < b:
    e = a + (a * 10/100)
    d = d + 1
    print(d, "-й день результат: ", float('{:.2f}'.format(e)))
    a = e
    if e > b:
        print("На", d, "-й день спортсмен достиг результата - не менее", int(e), "километров")
        break
